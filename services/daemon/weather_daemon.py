        while True:
            interval_hours = DaemonManager.get_interval()
            logging.info(f"üîç –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–ø–∏—Å—á–∏–∫–æ–≤...")
            
