        # –ü–æ–ª—É—á–∞–µ–º –ø—Ä–æ–≥–Ω–æ–∑
        from config.settings import OPENWEATHER_API_KEY
        weather_client = WeatherAPIClient(api_key=OPENWEATHER_API_KEY)
        forecast = weather_client.get_forecast(city)
        
        if not forecast:
            logging.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –ø—Ä–æ–≥–Ω–æ–∑ –¥–ª—è {city}")
            return False
            
        # –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –∏ –ø–æ–ª—É—á–∞–µ–º —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—é - –∏—Å–ø–æ–ª—å–∑—É–µ–º –Ω–æ–≤—ã–π –º–µ—Ç–æ–¥
        analyzer = WeatherAnalyzer(forecast)
        recommendation = analyzer.get_detailed_recommendation()
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
        message = (
            f"üå§ *–ï–∂–µ–¥–Ω–µ–≤–Ω—ã–π –ø—Ä–æ–≥–Ω–æ–∑ –¥–ª—è {city}:*\n\n"
            f"{recommendation}"
        )
        
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —á–µ—Ä–µ–∑ –±–æ—Ç–∞
        bot.send_message(
            chat_id, 
            message,
            parse_mode='Markdown'
        )
        
        logging.info(f"‚úÖ –£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ –¥–ª—è {city}")
